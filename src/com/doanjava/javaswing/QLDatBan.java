    private boolean kiemtraNull(){
        if(tfMaKH.getText().equals("")){
            JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa nhập mã khách hàng!");
            return false;
        }
        else
        if(tfTenkhach.getText().equals("")){
            JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa nhập tên khách hàng!");
            return false;
        }
        else
        if(rdoKhong.isSelected()==false && rdoDathanhtoan.isSelected()==false){
            JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa chọn trạng thái thanh toán!");
            return false;
        }
        else
        if(((JTextField)tfNgay.getDateEditor().getUiComponent()).getText().equals("")){
            JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa nhập ngày khách đặt!");
            return false;
        }
        else   
        if(tfSDT.getText().equals("")){
            JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa nhập số điện thoại!");
            return false;
        }
        else   
        if(tfGhiChu.getText().equals("")){
            JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa ghi chú!");
            return false;
        }
        else
        if(cbSoBan.getSelectedItem().equals("")){
           JOptionPane.showMessageDialog(QLDatBan.this,"Bạn chưa chọn bàn!");
            return false;
        }
        else{
            return true;
        }
    }
    
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if(kiemtraNull()){
        datBan.setMaKH(tfMaKH.getText());                            
        datBan.setTenKhachHang(tfTenkhach.getText());                
        datBan.setSoDT(tfSDT.getText());         
        datBan.setMaSoBan(cbSoBan.getSelectedItem().toString());
        datBan.setNgayDat(new java.sql.Date(tfNgay.getDate().getTime()).toString());
        String trangThai;
        if(rdoKhong.isSelected()){
            trangThai = "Không";
        }else
            trangThai = "Đã thanh toán";
        datBan.setTrangThaiDat(trangThai);            
        datBan.setGhiChu(tfGhiChu.getText());  
        
        
        datBanSERVICE.themDatBan(datBan); 
        datBanSERVICE.capNhatTTBan(datBan); //Cập nhật trạng thái Bàn = Khách đặt
        JOptionPane.showMessageDialog(null, "Đã thêm bàn đặt mới thành công!^^");
        Refesh();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

