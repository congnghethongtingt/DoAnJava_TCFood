    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        btnLuu.setEnabled(true);
        tfMaKH.setEnabled(false);
        int row = tableDatban.getSelectedRow();   
        if(row == -1){
            JOptionPane.showMessageDialog(QLDatBan.this, "Vui lòng chọn bàn cần sửa","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else{
            int xacNhan = JOptionPane.showConfirmDialog(QLDatBan.this, "Bạn có chắc chắn muốn sửa không?","Thông báo",JOptionPane.YES_NO_OPTION);
            if(xacNhan == JOptionPane.YES_OPTION){
                
            datBan.setMaKH(tfMaKH.getText());
            datBan.setTenKhachHang(tfTenkhach.getText());
            datBan.setMaSoBan(cbSoBan.getItemAt(cbSoBan.getSelectedIndex()));
            datBan.setNgayDat(new java.sql.Date(tfNgay.getDate().getTime()).toString());
            datBan.setSoDT(tfSDT.getText());             
            String trangThai;
            if(rdoKhong.isSelected()){
                trangThai = "Không";
            }else
                trangThai = "Đã thanh toán";
                datBan.setTrangThaiDat(trangThai);
                datBan.setGhiChu(tfGhiChu.getText());
        
                String maBan = String.valueOf(tableDatban.getValueAt(row, 0));
                datBanSERVICE.suaDatBan(datBan);
            JOptionPane.showMessageDialog(null, "Đã sửa bàn đặt thành công!^^");
                Refesh();
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tableDatban.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(QLDatBan.this, "Vui lòng chọn bàn cần xóa","Thông báo",JOptionPane.ERROR_MESSAGE);
        }else{
            int xacNhan = JOptionPane.showConfirmDialog(QLDatBan.this, "Bạn có chắc chắn muốn xóa không?","Thông báo",JOptionPane.YES_NO_OPTION);
            if(xacNhan == JOptionPane.YES_OPTION){
                datBan.setMaKH(tfMaKH.getText());
                datBan.setMaSoBan(cbSoBan.getItemAt(cbSoBan.getSelectedIndex()));
                String maBan = String.valueOf(tableDatban.getValueAt(row, 0));
                datBanSERVICE.xoaDatBan(maBan);
                datBanSERVICE.TTBanTrong(datBan); //Cập nhật trạng thái Bàn = Khách đặt
                JOptionPane.showMessageDialog(null, "Đã xóa bàn đặt thành công!^^");
            }
        }
        Refesh();
    }//GEN-LAST:event_btnXoaActionPerformed

